import 'package:lib_sj/common/app_setting.dart';

String? getSjTextByKey(String key) {
  switch (AppSetting.locale) {
    case "zh_CN":
    case "zh_Hans_CN":
      return zh_hans_map[key] ?? zh_hans_map['unknown_error'];
    case "zh_Hant_HK":
    case "zh_Hant_TW":
    case "zh_Hant_CN":
    case "zh_TW":
    case "zh_HK":
      return zh_hant_map[key] ?? zh_hant_map['unknown_error'];
    case "en_US":
    case "en_UK":
    case "en":
      return en_map[key] ?? en_map['unknown_error'];
  }
  return zh_hans_map[key] ?? zh_hans_map['unknown_error'];
}

